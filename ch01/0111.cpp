#include "iostream"

using namespace std;

int main()
{
    int x;
    int y;

    cout << "the four arithmetical operations of x and y\n";

    cout << "input x and y: ";
    cin >> x >> y;

    cout << "x + y is " << x + y << "\n";
    cout << "x - y is " << x - y << "\n";
    cout << "x * y is " << x * y << "\n";
    cout << "x / y is " << x / y << "\n";
    cout << "x % y is " << x % y << "\n";
}
